/*************************************************************************************************/
/*!
	\brief
		Manages scenes in the game
*/
/*************************************************************************************************/
class SceneManager : public System
{
public:
	//---------------------------------------------------------------------------------------------
	// Public Consts
	//---------------------------------------------------------------------------------------------

	// Scene id rules:
	// Negatives: Debug scenes
	// 0-99: UI scene
	// 100+: Gameplay scenes (e.g. 1-2 is 102)
	
	//---------------------------------------------------------------------------------------------
	// Public Structures
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Public Variables
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Public Function Declarations
	//---------------------------------------------------------------------------------------------

	/*************************************************************************************************/
	/*!
		\brief
			Constructor for the texture manager class
	*/
	/*************************************************************************************************/
	SceneManager();

	/*************************************************************************************************/
	/*!
		\brief
			Destructor for the scene manager class
	*/
	/*************************************************************************************************/
	~SceneManager() {}

	/*************************************************************************************************/
	/*!
		\brief
			Initializes the scene manager (defaults to: sandbox)
	*/
	/*************************************************************************************************/
	void Init();

	/*************************************************************************************************/
	/*!
		\brief
			Updates the current scene

		\param
			The time elapsed since the previous frame.
	*/
	/*************************************************************************************************/
	void Update(double dt);

	/*************************************************************************************************/
	/*!
		\brief
			Draws the current scene (currently empty)
	*/
	/*************************************************************************************************/
	void Draw();

	/*************************************************************************************************/
	/*!
		\brief
			Shuts down the scene manager and the current scene
	*/
	/*************************************************************************************************/
	void Shutdown();

	/*************************************************************************************************/
	/*!
		\brief
			Sets this scene to be changed to

		\param sceneID
			The id of the new scene
	*/
	/*************************************************************************************************/
	void ChangeScene(int sceneID);

	/*************************************************************************************************/
	/*!
		\brief
			Finds the first listed scene with the given id

		\param sceneID
			The id of the scene

		\return
			The requested scene. Returns NULL if no matching id was found.
	*/
	/*************************************************************************************************/
	Scene* FindScene(int sceneID);

	/*************************************************************************************************/
	/*!
		\brief
			Gets the current scene being run

		\return
			The current scene
	*/
	/*************************************************************************************************/
	Scene* GetCurrentScene() { return sceneList[currentSceneIndex]; }
	
private:
	//---------------------------------------------------------------------------------------------
	// Private Consts
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Private Structures
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Private Variables
	//---------------------------------------------------------------------------------------------

	std::vector<Scene*> sceneList;				// The list of scenes
	int currentSceneIndex;						// The index of the current scene
	int nextSceneIndex;							// The index of the next scene
	bool changeScene;							// Boolean telling whether the scene should change
	
	//---------------------------------------------------------------------------------------------
	// Private Function Declarations
	//---------------------------------------------------------------------------------------------

	/*************************************************************************************************/
	/*!
		\brief
			Finds the first listed scene index with the given id

		\param sceneID
			The id of the scene

		\return
			The requested scene index. Returns -1 if no matching id was found.
	*/
	/*************************************************************************************************/
	int FindSceneIndex(int sceneID);
};

