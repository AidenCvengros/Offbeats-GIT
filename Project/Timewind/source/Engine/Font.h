/*************************************************************************************************/
/*!
	\brief
		The font class. It's a wrapper for holding font textures.
*/
/*************************************************************************************************/
class Font
{
public:
	//---------------------------------------------------------------------------------------------
	// Public Consts
	//---------------------------------------------------------------------------------------------

	struct Character
	{
		glm::ivec2 size;						// Size of glyph
		glm::ivec2 bearing;						// Offset from baseline to left/top of glyph
		float offset;							// The horizontal offset of this character in the texture
		unsigned int advance;					// Offset to advance to next glyph
	};
	
	//---------------------------------------------------------------------------------------------
	// Public Structures
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Public Variables
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Public Function Declarations
	//---------------------------------------------------------------------------------------------

	/*************************************************************************************************/
	/*!
		\brief
			Constructor for the font class

		\param filename_
			The name and location of the font file
	*/
	/*************************************************************************************************/
	Font(std::string filename_);

	/*************************************************************************************************/
	/*!
		\brief
			Destructor for the camera class
	*/
	/*************************************************************************************************/
	~Font();

	/*************************************************************************************************/
	/*!
		\brief
			Frees the font from memory
	*/
	/*************************************************************************************************/
	void Free();

	/*************************************************************************************************/
	/*!
		\brief
			Returns the texture for the font

		\return
			The texture
	*/
	/*************************************************************************************************/
	Texture* GetTexture() { return texture; }

	/*************************************************************************************************/
	/*!
		\brief
			Returns the inverted width of the texture aka the width of a character

		\return
			The inverted bitmap width
	*/
	/*************************************************************************************************/
	float GetInvertedBmpWidth() { return invertedBmpWidth; }

	/*************************************************************************************************/
	/*!
		\brief
			Returns the max height of the font

		\return
			The bitmap height
	*/
	/*************************************************************************************************/
	float GetBmpHeight() { return bmpHeight; }

	/*************************************************************************************************/
	/*!
		\brief
			Returns the font data for the given character

		\param character
			The given character

		\return
			That character's font data
	*/
	/*************************************************************************************************/
	Character GetCharacter(char character) { return characters[character]; }
	
private:
	//---------------------------------------------------------------------------------------------
	// Private Consts
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Private Structures
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Private Variables
	//---------------------------------------------------------------------------------------------
	
	std::string filename;						// The name of the texture file
	Texture* texture;							// The font texture

	std::map<char, Character> characters;		// The list of character structs

	unsigned int bmpHeight;						// The max height to allow the font
	float invertedBmpWidth;						// The inverse of the max height aka 

	//---------------------------------------------------------------------------------------------
	// Private Function Declarations
	//---------------------------------------------------------------------------------------------

};

//-------------------------------------------------------------------------------------------------
