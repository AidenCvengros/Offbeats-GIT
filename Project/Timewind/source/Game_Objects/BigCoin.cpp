/*************************************************************************************************/
/*!
	\brief
		Clears the tiles that the big coin is occupying
*/
/*************************************************************************************************/
void BigCoin::ClearTiles()
{
    // Gets this objects map coordinates
    std::pair<int, int> mapCoords = GetMapCoords();
    _MapMatrix->ClearTile(mapCoords);
    _MapMatrix->ClearTile(mapCoords.first + 1, mapCoords.second);
    _MapMatrix->ClearTile(mapCoords.first + 1, mapCoords.second + 1);
    _MapMatrix->ClearTile(mapCoords.first, mapCoords.second + 1);

    // Sets this object to be destroyed
    SetToBeDestroyed(true);
}

