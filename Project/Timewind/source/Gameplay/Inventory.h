// Base include
#include "../Engine/stdafx.h"

// Inventory objects are stored in an array container
#include <array>

//-------------------------------------------------------------------------------------------------
// Forward References
//-------------------------------------------------------------------------------------------------

// Dependency reference
class Key;
class Sticker;

//-------------------------------------------------------------------------------------------------
// Public Constants
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
// Public Classes
//-------------------------------------------------------------------------------------------------

/*************************************************************************************************/
/*!
	\brief
		The inventory manager class
*/
/*************************************************************************************************/
class Inventory
{
public:
	//---------------------------------------------------------------------------------------------
	// Public Consts
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Public Structures
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Public Variables
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Public Function Declarations
	//---------------------------------------------------------------------------------------------
	
	/*************************************************************************************************/
	/*!
		\brief
			Constructor for the inventory class
	*/
	/*************************************************************************************************/
	Inventory() : keyList(), coinCount(0), stickerList(), stickerInventoryCursor(0) {}

	/*************************************************************************************************/
	/*!
		 \brief
			Destructor for inventory class
	*/
	/*************************************************************************************************/
	~Inventory() {}

	/*************************************************************************************************/
	/*!
		\brief
			Adds the given item to the inventory

		\param newItem
			The new item to be added into the inventory
	*/
	/*************************************************************************************************/
	//void AddItem(Item* newItem) { itemList.push_back(newItem); }

	/*************************************************************************************************/
	/*!
		\brief
			Adds the given key to the inventory

		\param newKey
			The new key to be added into the inventory

		\return
			Returns true if the key was added. False if not
	*/
	/*************************************************************************************************/
	bool AddKey(Key* newKey);

	/*************************************************************************************************/
	/*!
		\brief
			Adds the given sticker to the inventory

		\param newSticker
			The new sticker to be added into the inventory

		\return
			Returns true if the sticker was added. False if not
	*/
	/*************************************************************************************************/
	bool AddSticker(Sticker* newSticker);

	/*************************************************************************************************/
	/*!
		\brief
			Adds the given key to the inventory

		\return
			Returns true if coin count hit 100.
	*/
	/*************************************************************************************************/
	bool AddCoin();

	/*************************************************************************************************/
	/*!
		\brief
			Checks whether the player has a key of the given index

		\param keyIndex
			The index of the key being checked

		\return
			Whether the player has the key
	*/
	/*************************************************************************************************/
	bool HaveKey(int keyIndex) { return keyList[keyIndex] != NULL; }

	/*************************************************************************************************/
	/*!
		\brief
			Increments the selected sticker one to the right
	*/
	/*************************************************************************************************/
	void IncrementSelectedSticker();

	/*************************************************************************************************/
	/*!
		\brief
			Decrements the selected sticker one to the left
	*/
	/*************************************************************************************************/
	void DecrementSelectedSticker();

	/*************************************************************************************************/
	/*!
		\brief
			Returns the sticker at the selected index

		\return
			The chosen sticker
	*/
	/*************************************************************************************************/
	Sticker* GetSelectedSticker() { return stickerList[stickerInventoryCursor]; }

	/*************************************************************************************************/
	/*!
		\brief
			Clears the sticker at the selected index
	*/
	/*************************************************************************************************/
	void ClearSelectedSticker() { stickerList[stickerInventoryCursor] = NULL; }

private:
	//---------------------------------------------------------------------------------------------
	// Private Consts
	//---------------------------------------------------------------------------------------------

	//std::vector<Item*> itemList;				// The list of general items the player has
	std::array<Key*, 56> keyList;				// The list of keys the player has collected
	int coinCount;								// The number of coins the player has collected

	std::array<Sticker*, 10> stickerList;		// The list of stickers the player has currently
	int stickerInventoryCursor;					// What sticker slot the player currently has selected
	
	//---------------------------------------------------------------------------------------------
	// Private Structures
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Private Variables
	//---------------------------------------------------------------------------------------------
	
	//---------------------------------------------------------------------------------------------
	// Private Function Declarations
	//---------------------------------------------------------------------------------------------
};

//-------------------------------------------------------------------------------------------------
